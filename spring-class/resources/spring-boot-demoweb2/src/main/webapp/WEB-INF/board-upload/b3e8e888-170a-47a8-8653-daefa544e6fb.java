
public class HelloWorld {

	public static void main(String[] args) { //Entry Point
		
		System.out.println("Hello, Eclipse Java Programming World !!!");
		System.out.println("Bye"); //메시지 출력
		
	}

}

//구문규칙
//1. 클래스와 같은 타입을 제외한 모든 프로그램 요소는 타입 내부에 포함되어야 합니다.
//2. 프로그램은 static void main(String[] args) 메서드에서 시작됩니다.
//3. main 메서드가 종료되면 프로그램도 종료됩니다.
//4. 대소문자를 구분합니다.
//5. 문장의 끝은 ;(세미콜론)으로 표시합니다.
//6. 주석 : 컴파일러가 컴파일하지 않는 영역
// - 라인주석 : //여기서부터 이줄의 끝까지 주석입니다.
// - 영역주석 : /* 이 내용은 주석으로 처리됩니다. */
